package com.warehouse.util;

/**
 * 代码 的大小写转换
 * 
 * @author Beilaile
 *
 */
public class CharacterUtils {
	/**
	 * 把小写转成大写
	 * 
	 * @param data
	 * @return
	 */
	public static String getUpperCase(String data) {
		return data.toUpperCase();
	}

	/**
	 * 把大写转成小写
	 * 
	 * @param data
	 * @return
	 */
	public static String getLowerCase(String data) {
		return data.toLowerCase();
	}

}
